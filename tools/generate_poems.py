def get_char_pinyin(char: str, poem_title: str) -> str:
    overrides = POEM_TONE_OVERRIDES.get(poem_title, {})
    if char in overrides:
        return overrides[char]
    try:
        result = pinyin(char, style=PinyinStyle.TONE)
        if result and result[0]:
            return result[0][0]
    except Exception:
        pass
    return char

def line_to_ruby(line: str, poem_title: str) -> str:
    cleaned = line.strip()
    if not cleaned:
        return ""
    parts = []
    for ch in cleaned:
        if ch.strip() and not ch.isspace():
            py = get_char_pinyin(ch, poem_title)
            parts.append(f"<ruby>{ch}<rt>{py}</rt></ruby>")
        else:
            parts.append(ch)
    return "".join(parts)

def make_lines(lines: list[str], poem_title: str) -> str:
    out = []
    for line in lines:
        ruby = line_to_ruby(line, poem_title)
        out.append(f'    <p class="poem-line">{ruby}</p>')
    return "\n".join(out)

def generate_poem(grade, title, poet, dynasty, type_, theme, tags=None, lines=None, translation="", notes="", analysis="", author=""):
    grade_label = grade[3:]
    tag_lines = "\n".join(f"  - {t}" for t in (tags or []))
    if lines:
        ruby_lines = make_lines(lines, title)
    else:
        ruby_lines = '    <p class="poem-line">\uff08\u5f85\u8865\u5145\u5168\u6587\uff09</p>'
    sections = []
    if translation:
        sections.append(f"## \u8bd1\u6587\n\n{translation}")
    if notes:
        sections.append(f"## \u6ce8\u91ca\n\n{notes}")
    if analysis:
        sections.append(f"## \u8d4f\u6790\n\n{analysis}")
    if author:
        sections.append(f"## \u4f5c\u8005\n\n{author}")
    sections_str = "\n\n".join(sections)
    return TEMPLATE.format(title=title, poet=poet, dynasty=dynasty, type_=type_, theme=theme, grade=grade_label, tags=tag_lines, lines=ruby_lines, sections=sections_str)


