def main():
    generated = 0
    skipped = 0
    errors = []
    for grade, poems in POEMS_BY_GRADE.items():
        grade_dir = BASE_DIR / grade
        grade_dir.mkdir(parents=True, exist_ok=True)
        for poem in poems:
            filepath = grade_dir / f"{poem['title']}.md"
            if filepath.exists():
                print(f"  SKIP {grade} / {poem['title']} - already exists")
                skipped += 1
                continue
            try:
                content = generate_poem(grade, **poem)
                filepath.write_text(content, encoding="utf-8")
                print(f"  OK   {grade} / {poem['title']}")
                generated += 1
            except Exception as e:
                print(f"  ERR  {grade} / {poem['title']}: {e}")
                errors.append(f"{grade}/{poem['title']}: {e}")
    print(f"\nDone. Generated: {generated}, Skipped: {skipped}, Errors: {len(errors)}")
    for err in errors:
        print(f"  ERROR: {err}")
    return generated, skipped, errors


if __name__ == "__main__":
    main()
