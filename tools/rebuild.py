def poem_path(title: str, grade_dir: str) -> str:
    safe = title.replace("（", "(").replace("）", ")").replace("《", "").replace("》", "")
    return f"{grade_dir}/{safe}/"

def make_poems_json():
    """Export all poems as JSON."""
    poems = []
    seen = set()
    for meta in POEMS_META:
        title, poet, dynasty, type_, theme, grade_dir, tags = meta
        grade_label = GRADE_LABELS.get(grade_dir, grade_dir)
        semester = get_semester(grade_dir, title)
        p = poem_path(title, grade_dir)
        key = (grade_dir, title)
        if key in seen:
            continue
        seen.add(key)
        poems.append({
            "title": title,
            "poet": poet,
            "dynasty": dynasty,
            "type": type_,
            "theme": theme,
            "grade": grade_dir,
            "grade_label": grade_label,
            "semester": semester,
            "path": p,
            "tags": tags,
        })
    out = BASE_DIR / "assets" / "data" / "poems.json"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(poems, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"  poems.json → {len(poems)} 篇")
    return poems

def find_existing_md() -> set[tuple[str, str]]:
    """Return set of (grade_dir, title) for existing .md files."""
    found = set()
    for g in GRADE_ORDER:
        d = BASE_DIR / g
        if not d.exists():
            continue
        for f in d.iterdir():
            if f.suffix != ".md" or f.name == "index.md":
                continue
            title = f.stem
            found.add((g, title))
    return found

def fill_missing_md(existing: set[tuple[str, str]], poems_data: list[dict]):
    """Create minimal .md for missing poems."""
    # Build map from (grade, title) -> full poem data from generator
    # We'll import the generator's detailed data for poems that have it
    import importlib.util
    spec = importlib.util.spec_from_file_location("gen", BASE_DIR.parent / "tools" / "generate_poems.py")
    gen = importlib.util.module_from_spec(spec)
    try:
        spec.loader.exec_module(gen)
        gen_poems = gen.POEMS_BY_GRADE
    except:
        gen_poems = {}

    # Build gen map: (grade, title) -> poem dict
    gen_map: dict = {}
    for g, plist in gen_poems.items():
        for p in plist:
            gen_map[(g, p["title"])] = p

    created = 0
    for p in poems_data:
        key = (p["grade"], p["title"])
        if key in existing:
            continue
        # Check if generator has detailed content
        gd = gen_map.get(key)
        if gd and gd.get("lines"):
            # Use generator to create full content
            content = gen.generate_poem(p["grade"], **gd)
        else:
            content = make_minimal_md(p)
        filepath = BASE_DIR / p["grade"] / f"{p['title']}.md"
        filepath.parent.mkdir(parents=True, exist_ok=True)
        # Don't overwrite if it now exists (e.g. from generator having run)
        if filepath.exists():
            continue
        filepath.write_text(content, encoding="utf-8")
        created += 1
        print(f"  + {p['grade']} / {p['title']}")
    print(f"  Created {created} missing files")
    return created

