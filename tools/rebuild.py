def get_semester(grade_dir: str, title: str) -> str:
    """Return '上', '下', or '' for a poem."""
    smap = SEMESTER_MAP.get(grade_dir, {})
    for sem, titles in smap.items():
        if title in titles:
            return sem
    return ""

